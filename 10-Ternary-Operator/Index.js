
// Ternary Operator
const age = 18;
 age >= 18 ? console.log('Adult') : console.log('Not Adult'); // Output: Adult

// If Else Statement
if (age >= 18) {
  console.log('Adult');
} else {
    console.log('Not Adult');
}

// Ternary Operator
console.log(age >= 18 ? 'Adult' : 'Not Adult'); // Output: Adult